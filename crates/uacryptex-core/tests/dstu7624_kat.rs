#[test]
fn dstu7624_ecb_encrypt_kat_per_vector() {
    for (i, v) in ENC_ECB_DATA.iter().enumerate() {
        let key = decode_le_hex(v.key);
        let data = decode_le_hex(v.data);
        let exp = decode_le_hex(v.exp);
        let ctx = Dstu7624Ecb::init(&key, data.len()).unwrap();
        let act = ctx.encrypt(&data).unwrap();
        assert_eq!(act, exp, "encrypt vector {i}");
    }
}

#[test]
fn dstu7624_ecb_encrypt_kat() {
    for (i, v) in ENC_ECB_DATA.iter().enumerate() {
        let key = decode_le_hex(v.key);
        let data = decode_le_hex(v.data);
        let exp = decode_le_hex(v.exp);
        let ctx =
            Dstu7624Ecb::init(&key, data.len()).unwrap_or_else(|e| panic!("vector {i} init: {e}"));
        let act = ctx
            .encrypt(&data)
            .unwrap_or_else(|e| panic!("vector {i} encrypt: {e}"));
        assert_eq!(act, exp, "encrypt mismatch at vector {i}");
    }
}

#[test]
fn dstu7624_ecb_decrypt_roundtrip() {
    for (i, v) in ENC_ECB_DATA.iter().enumerate() {
        let key = decode_le_hex(v.key);
        let data = decode_le_hex(v.data);
        let exp = decode_le_hex(v.exp);
        let ctx =
            Dstu7624Ecb::init(&key, data.len()).unwrap_or_else(|e| panic!("vector {i} init: {e}"));
        let dec = ctx
            .decrypt(&exp)
            .unwrap_or_else(|e| panic!("vector {i} decrypt: {e}"));
        assert_eq!(dec, data, "decrypt mismatch at vector {i}");
    }
}


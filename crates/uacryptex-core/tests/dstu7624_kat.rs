fn iso7816_pad(data: &[u8], block_len: usize) -> Vec<u8> {
    let pad_len = block_len - data.len() % block_len;
    let mut out = data.to_vec();
    out.push(0x80);
    out.resize(out.len() + pad_len - 1, 0);
    out
}

fn iso7816_unpad(data: &[u8]) -> Vec<u8> {
    let mut i = data.len().saturating_sub(1);
    while i > 0 && data[i] == 0 {
        i -= 1;
    }
    data[..i].to_vec()
}

